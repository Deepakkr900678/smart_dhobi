/**
 * PATCH /api/order/:id/status
 * Update an order's status. Enforces strict FSM transitions and notifies both parties.
 * @access Private (Dhobi, Admin)
 */
exports.updateOrderStatus = async (req, res) => {
  if (!validateId(req.params.id, res, "Order")) return;

  try {
    const { status } = req.body;

    if (!status) {
      return res.status(400).json({ success: false, message: "Status is required" });
    }

    if (!VALID_ORDER_STATUSES.includes(status)) {
      return res.status(400).json({
        success: false,
        message: `Invalid status. Valid values: ${VALID_ORDER_STATUSES.join(", ")}`,
      });
    }

    const order = await Order.findById(req.params.id);
    if (!order) {
      return res.status(404).json({ success: false, message: "Order not found" });
    }

    // ── FSM transition check ─────────────────────────────────────────────────
    const allowedNext = STATUS_FSM[order.status] ?? [];
    if (!allowedNext.includes(status)) {
      return res.status(400).json({
        success: false,
        message:
          allowedNext.length
            ? `Cannot transition from "${order.status}" to "${status}". Allowed: ${allowedNext.join(", ")}`
            : `Order is already "${order.status}" and cannot be updated`,
      });
    }

    // ── Ownership check for dhobi ────────────────────────────────────────────
    if (req.user.role === "dhobi") {
      const provider = await ServiceProvider.findOne({ userId: req.user.id })
        .select("_id")
        .lean();

      if (!provider || provider._id.toString() !== order.providerId.toString()) {
        return res.status(403).json({
          success: false,
          message: "This order does not belong to your account",
        });
      }
    }

    order.status = status;
    await order.save();

    // ── Notify customer ──────────────────────────────────────────────────────
    const customerMsg = STATUS_MESSAGES[status]?.(order.orderId)
      ?? `Your order #${order.orderId} has been updated to: ${status}`;
    notify(order.userId, customerMsg, order._id, "order");

    // ── Notify provider if customer/admin cancels ────────────────────────────
    if (status === "cancelled" && req.user.role !== "dhobi") {
      const provider = await ServiceProvider.findById(order.providerId).select("userId").lean();
      if (provider?.userId) {
        notify(
          provider.userId,
          `Order #${order.orderId} has been cancelled.`,
          order._id,
          "order"
        );
      }
    }

    return res.status(200).json({
      success: true,
      message: "Order status updated successfully",
      data: { _id: order._id, orderId: order.orderId, status: order.status },
    });
  } catch (err) {
    console.error("[updateOrderStatus]", err);
    return res.status(500).json({ success: false, message: "Internal server error" });
  }
};

