/**
 * POST /api/order/create-razorpay-order
 * Creates a Razorpay payment order for an existing platform order.
 * @access Private (User)
 */
exports.createRazorpayOrder = async (req, res) => {
  try {
    if (!process.env.RAZORPAY_KEY_ID || !process.env.RAZORPAY_KEY_SECRET) {
      console.error("[createRazorpayOrder] Razorpay keys missing from environment");
      return res.status(500).json({ success: false, message: "Payment service is not configured" });
    }

    const { orderId } = req.body;

    if (!orderId?.trim()) {
      return res.status(400).json({ success: false, message: "orderId is required" });
    }

    const order = await Order.findOne({ orderId })
      .populate("userId", "name email mobile")
      .populate("providerId", "name");

    if (!order) {
      return res.status(404).json({ success: false, message: "Order not found" });
    }

    // Authorization: only the order's user can initiate payment
    if (order.userId._id.toString() !== req.user.id.toString()) {
      return res.status(403).json({ success: false, message: "Access denied" });
    }

    if (order.paymentStatus === "completed") {
      return res.status(409).json({ success: false, message: "Payment already completed for this order" });
    }

    if (["cancelled"].includes(order.status)) {
      return res.status(400).json({ success: false, message: "Cannot initiate payment for a cancelled order" });
    }

    // Amount in paise (Razorpay requires integer paise, no floats)
    const amountPaise = Math.round(order.amount * 100);

    const razorpayOrder = await razorpay.orders.create({
      amount: amountPaise,
      currency: "INR",
      receipt: `rcpt_${orderId}_${Date.now()}`.slice(0, 40), // Razorpay receipt max 40 chars
      notes: {
        orderId: order.orderId,
        userId: order.userId._id.toString(),
        providerId: order.providerId._id.toString(),
      },
    });

    // Persist Razorpay order ID for later signature verification
    await Order.findByIdAndUpdate(order._id, { razorpayOrderId: razorpayOrder.id });

    return res.status(201).json({
      success: true,
      data: {
        razorpayOrderId: razorpayOrder.id,
        key: process.env.RAZORPAY_KEY_ID,
        amount: razorpayOrder.amount,
        currency: razorpayOrder.currency,
        order: {
          _id: order._id,
          orderId: order.orderId,
          customerName: order.userId.name,
          customerEmail: order.userId.email,
          customerContact: order.userId.mobile,
        },
      },
    });
  } catch (err) {
    console.error("[createRazorpayOrder]", err);
    const msg = err?.error?.description ?? err?.description ?? err?.message;
    return res.status(err?.statusCode ?? 500).json({
      success: false,
      message: msg ?? "Failed to create payment order",
    });
  }
};

/**
 * POST /api/order/verify-payment
 * Verifies Razorpay signature and marks order as paid.
 * Uses timing-safe comparison to prevent signature forgery.
 * @access Private (User)
 */
exports.verifyPayment = async (req, res) => {
  try {
    const { orderId, razorpayPaymentId, razorpayOrderId, razorpaySignature } = req.body;

    if (!orderId || !razorpayPaymentId || !razorpayOrderId || !razorpaySignature) {
      return res.status(400).json({ success: false, message: "Missing required payment fields" });
    }

    const order = await Order.findOne({ orderId });
    if (!order) {
      return res.status(404).json({ success: false, message: "Order not found" });
    }

    if (order.userId.toString() !== req.user.id.toString()) {
      return res.status(403).json({ success: false, message: "Access denied" });
    }

    if (order.paymentStatus === "completed") {
      return res.status(409).json({ success: false, message: "Payment already verified" });
    }

    // Verify that the razorpayOrderId matches what we stored
    if (order.razorpayOrderId !== razorpayOrderId) {
      return res.status(400).json({ success: false, message: "Razorpay order ID mismatch" });
    }

    // ── Signature verification (timing-safe) ─────────────────────────────────
    const expectedSig = crypto
      .createHmac("sha256", process.env.RAZORPAY_KEY_SECRET)
      .update(`${razorpayOrderId}|${razorpayPaymentId}`)
      .digest("hex");

    const sigValid = crypto.timingSafeEqual(
      Buffer.from(expectedSig),
      Buffer.from(razorpaySignature)
    );

    if (!sigValid) {
      await Order.findByIdAndUpdate(order._id, {
        paymentStatus: "failed",
        failureReason: "Invalid payment signature",
      });
      return res.status(400).json({ success: false, message: "Payment verification failed: invalid signature" });
    }

    // ── Mark order as paid ───────────────────────────────────────────────────
    await Order.findByIdAndUpdate(order._id, {
      paymentStatus: "completed",
      razorpayPaymentId,
      razorpaySignature, // stored with select:false — never returned in responses
      paidAt: new Date(),
    });

    // Notify provider about payment
    const provider = await ServiceProvider.findById(order.providerId).select("userId").lean();
    if (provider?.userId) {
      notify(
        provider.userId,
        `Payment received for order #${order.orderId}.`,
        order._id,
        "order"
      );
    }

    return res.status(200).json({
      success: true,
      message: "Payment verified successfully",
      data: {
        orderId: order.orderId,
        paymentStatus: "completed",
        paidAt: new Date(),
      },
    });
  } catch (err) {
    console.error("[verifyPayment]", err);
    return res.status(500).json({ success: false, message: "Payment verification failed" });
  }
};

