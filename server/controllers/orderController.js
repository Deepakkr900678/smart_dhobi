/**
 * POST /api/order/create
 * User places a new order with a selected provider.
 * orderId is auto-generated by the Order model pre-validate hook.
 * @access Private (User)
 */
exports.createOrder = async (req, res) => {
  try {
    const {
      providerId,
      services,
      pickupAddress,
      deliveryAddress,
      pickupTime,
      deliveryTime,
      amount,
      pickupLocation,
      deliveryLocation,
    } = req.body;

    const userId = req.user.id;

    // ── Required field validation ────────────────────────────────────────────
    const missing = [];
    if (!providerId) missing.push("providerId");
    if (!services?.length) missing.push("services");
    if (!pickupAddress?.trim()) missing.push("pickupAddress");
    if (!deliveryAddress?.trim()) missing.push("deliveryAddress");
    if (!pickupTime) missing.push("pickupTime");
    if (!deliveryTime) missing.push("deliveryTime");
    if (amount === undefined || amount === null) missing.push("amount");

    if (missing.length) {
      return res.status(400).json({
        success: false,
        message: `Missing required fields: ${missing.join(", ")}`,
      });
    }

    if (!isValidObjectId(providerId)) {
      return res.status(400).json({ success: false, message: "Invalid providerId" });
    }

    const parsedAmount = Number(amount);
    if (isNaN(parsedAmount) || parsedAmount <= 0) {
      return res.status(400).json({ success: false, message: "Amount must be a positive number" });
    }

    // ── Validate services array ──────────────────────────────────────────────
    for (const [i, svc] of services.entries()) {
      if (!svc.name?.trim()) {
        return res.status(400).json({ success: false, message: `Service at index ${i} is missing a name` });
      }
      if (typeof svc.quantity !== "number" || svc.quantity < 1) {
        return res.status(400).json({ success: false, message: `Service "${svc.name}" must have quantity ≥ 1` });
      }
      if (typeof svc.price !== "number" || svc.price < 0) {
        return res.status(400).json({ success: false, message: `Service "${svc.name}" must have a non-negative price` });
      }
      if (svc.customerPrice !== undefined && (typeof svc.customerPrice !== "number" || svc.customerPrice < 0)) {
        return res.status(400).json({
          success: false,
          message: `Service "${svc.name}" must have a non-negative customerPrice`,
        });
      }
      if (svc.dealerPrice !== undefined && (typeof svc.dealerPrice !== "number" || svc.dealerPrice < 0)) {
        return res.status(400).json({
          success: false,
          message: `Service "${svc.name}" must have a non-negative dealerPrice`,
        });
      }
    }

    // ── Provider checks ──────────────────────────────────────────────────────
    const provider = await ServiceProvider.findById(providerId).lean();
    if (!provider) {
      return res.status(404).json({ success: false, message: "Provider not found" });
    }
    if (!provider.isActive || provider.isApproved !== "approved") {
      return res.status(400).json({
        success: false,
        message: "This provider is not currently accepting orders",
      });
    }

    // ── Create order (orderId auto-generated by model hook) ──────────────────
    const orderData = {
      providerId,
      userId,
      services,
      pickupAddress: pickupAddress.trim(),
      deliveryAddress: deliveryAddress.trim(),
      pickupTime: new Date(pickupTime),
      deliveryTime: new Date(deliveryTime),
      amount: parsedAmount,
      status: "pending",
      paymentStatus: "pending",
    };

    // Optional geospatial fields
    if (pickupLocation?.coordinates?.length === 2) {
      orderData.pickupLocation = { type: "Point", coordinates: pickupLocation.coordinates };
    }
    if (deliveryLocation?.coordinates?.length === 2) {
      orderData.deliveryLocation = { type: "Point", coordinates: deliveryLocation.coordinates };
    }

    const order = await Order.create(orderData);

    // ── Notify provider (non-blocking) ───────────────────────────────────────
    notify(
      provider.userId,
      `You have a new order #${order.orderId} from a customer.`,
      order._id,
      "order"
    );

    return res.status(201).json({
      success: true,
      message: "Order created successfully",
      data: {
        _id: order._id,
        orderId: order.orderId,
        status: order.status,
        paymentStatus: order.paymentStatus,
        amount: order.amount,
      },
    });
  } catch (err) {
    console.error("[createOrder]", err);

    if (err.name === "ValidationError") {
      const messages = Object.values(err.errors).map((e) => e.message);
      return res.status(400).json({ success: false, message: messages.join(". ") });
    }

    return res.status(500).json({ success: false, message: "Internal server error" });
  }
};

/**
 * GET /api/order/userOrders
 * Returns the authenticated user's own orders.
 * @query page, limit, status
 * @access Private (User)
 */
exports.getUserOrders = async (req, res) => {
  try {
    const page = Math.max(1, parseInt(req.query.page, 10) || 1);
    const limit = Math.min(50, Math.max(1, parseInt(req.query.limit, 10) || 10));
    const skip = (page - 1) * limit;

    const filter = { userId: req.user.id };
    if (req.query.status && VALID_ORDER_STATUSES.includes(req.query.status)) {
      filter.status = req.query.status;
    }

    const [orders, total] = await Promise.all([
      Order.find(filter)
        .sort({ createdAt: -1 })
        .skip(skip)
        .limit(limit)
        .populate("providerId", "name email mobile address rating")
        .lean(),
      Order.countDocuments(filter),
    ]);

    return res.status(200).json({
      success: true,
      data: orders,
      pagination: {
        total,
        page,
        limit,
        totalPages: Math.ceil(total / limit),
        hasNextPage: page * limit < total,
        hasPrevPage: page > 1,
      },
    });
  } catch (err) {
    console.error("[getUserOrders]", err);
    return res.status(500).json({ success: false, message: "Internal server error" });
  }
};

