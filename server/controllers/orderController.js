/**
 * POST /api/order/webhook
 * Razorpay webhook handler. Must be mounted BEFORE express.json() on this route
 * so that req.body is the raw buffer for signature verification.
 * @access Public (signature-verified)
 */
exports.handleWebhook = async (req, res) => {
  try {
    const webhookSecret = process.env.RAZORPAY_WEBHOOK_SECRET;
    if (!webhookSecret) {
      console.error("[handleWebhook] RAZORPAY_WEBHOOK_SECRET not set");
      return res.status(500).json({ success: false });
    }

    const webhookSignature = req.get("X-Razorpay-Signature");
    if (!webhookSignature) {
      return res.status(400).json({ success: false, message: "Missing webhook signature" });
    }

    // req.body must be the raw buffer (use express.raw() on this route)
    const rawBody = req.body instanceof Buffer ? req.body : Buffer.from(JSON.stringify(req.body));

    const expectedSig = crypto
      .createHmac("sha256", webhookSecret)
      .update(rawBody)
      .digest("hex");

    const sigValid = crypto.timingSafeEqual(
      Buffer.from(expectedSig),
      Buffer.from(webhookSignature)
    );

    if (!sigValid) {
      console.warn("[handleWebhook] Invalid signature");
      return res.status(400).json({ success: false, message: "Invalid webhook signature" });
    }

    // Parse body only after signature is confirmed
    const payload = JSON.parse(rawBody.toString());
    const event = payload?.event;
    const paymentEntity = payload?.payload?.payment?.entity;
    const orderId = paymentEntity?.notes?.orderId;

    if (!orderId) {
      // Acknowledge unknown events so Razorpay doesn't retry
      return res.status(200).json({ success: true, message: "Event acknowledged (no orderId)" });
    }

    if (event === "payment.captured") {
      await Order.findOneAndUpdate(
        { orderId, paymentStatus: { $ne: "completed" } }, // idempotent
        {
          paymentStatus: "completed",
          razorpayPaymentId: paymentEntity.id,
          paidAt: new Date(),
        }
      );
    } else if (event === "payment.failed") {
      await Order.findOneAndUpdate(
        { orderId, paymentStatus: { $ne: "completed" } }, // don't overwrite a completed payment
        {
          paymentStatus: "failed",
          failureReason: paymentEntity.error_description ?? "Payment failed",
        }
      );
    }
    // All other events acknowledged silently

    return res.status(200).json({ success: true });
  } catch (err) {
    console.error("[handleWebhook]", err);
    // Return 200 to Razorpay to stop retries on unexpected errors
    return res.status(200).json({ success: false, message: "Webhook processing error" });
  }
};

/**
 * POST /api/order/refund
 * Admin initiates a refund via Razorpay. Full or partial.
 * Body: { orderId, amount? (partial), reason? }
 * @access Private (Admin)
 */
exports.refundPayment = async (req, res) => {
  try {
    const { orderId, amount, reason } = req.body;

    if (!orderId?.trim()) {
      return res.status(400).json({ success: false, message: "orderId is required" });
    }

    const order = await Order.findOne({ orderId });
    if (!order) {
      return res.status(404).json({ success: false, message: "Order not found" });
    }

    if (!order.razorpayPaymentId) {
      return res.status(400).json({ success: false, message: "No payment found for this order" });
    }

    if (order.paymentStatus !== "completed") {
      return res.status(400).json({
        success: false,
        message: `Cannot refund an order with payment status "${order.paymentStatus}"`,
      });
    }

    if (order.refundId) {
      return res.status(409).json({ success: false, message: "A refund has already been processed for this order" });
    }

    const refundOptions = {
      notes: { reason: reason?.trim() || "Admin-initiated refund", orderId },
    };

    // Partial refund: validate amount
    if (amount !== undefined) {
      const parsedAmount = Number(amount);
      if (isNaN(parsedAmount) || parsedAmount <= 0 || parsedAmount > order.amount) {
        return res.status(400).json({
          success: false,
          message: `Refund amount must be between 0 and ${order.amount}`,
        });
      }
      refundOptions.amount = Math.round(parsedAmount * 100); // paise
    }

    const refund = await razorpay.payments.refund(order.razorpayPaymentId, refundOptions);

    await Order.findByIdAndUpdate(order._id, {
      refundId: refund.id,
      refundAmount: refund.amount / 100,
      refundStatus: refund.status,
      refundedAt: new Date(),
      paymentStatus: "refunded",
    });

    // Notify the customer
    notify(
      order.userId,
      `Your refund for order #${order.orderId} of ₹${refund.amount / 100} has been initiated.`,
      order._id,
      "order"
    );

    return res.status(200).json({
      success: true,
      message: "Refund processed successfully",
      data: {
        refundId: refund.id,
        refundAmount: refund.amount / 100,
        refundStatus: refund.status,
      },
    });
  } catch (err) {
    console.error("[refundPayment]", err);
    const msg = err?.error?.description ?? err?.description ?? err?.message;
    return res.status(err?.statusCode ?? 500).json({
      success: false,
      message: msg ?? "Refund processing failed",
    });
  }
};
