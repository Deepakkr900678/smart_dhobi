/**
 * GET /api/order/:id
 * Get a single order.
 * Users see only their own; providers see only their assigned orders; admins see all.
 * @access Private
 */
exports.getOrderDetails = async (req, res) => {
  if (!validateId(req.params.id, res, "Order")) return;

  try {
    const order = await Order.findById(req.params.id)
      .populate("userId", "name email mobile")
      .populate("providerId", "name email mobile address")
      .lean();

    if (!order) {
      return res.status(404).json({ success: false, message: "Order not found" });
    }

    // ── Authorization ────────────────────────────────────────────────────────
    const isAdmin = req.user.role === "admin";
    const isOrderOwner = order.userId?._id?.toString() === req.user.id;

    // For provider: resolve their ServiceProvider._id
    let isOrderProvider = false;
    if (req.user.role === "dhobi") {
      const provider = await ServiceProvider.findOne({ userId: req.user.id })
        .select("_id")
        .lean();
      isOrderProvider = provider?._id?.toString() === order.providerId?._id?.toString();
    }

    if (!isAdmin && !isOrderOwner && !isOrderProvider) {
      return res.status(403).json({ success: false, message: "Access denied" });
    }

    return res.status(200).json({ success: true, data: order });
  } catch (err) {
    console.error("[getOrderDetails]", err);
    return res.status(500).json({ success: false, message: "Internal server error" });
  }
};

